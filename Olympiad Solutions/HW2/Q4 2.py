# a)Title (): Returns a copy of the strong in which all first characters are capitalized
# b)Upper (): Returns all the uppercase string from a given string. Converts all lowercase to uppercase
# c)Lower (): Returns the lowercase string from the given string. Converts all upper case characters to lowercase
# d)strip(): Deletes all the leading and trailing characters mentioned in its argument
# e)lstrip(): Deletes all the leading characters mentioned in the argument
# f)rstrip (): Deletes all the trailing characters mentioned in the argument

str = 'Today is a warm sunny day.'

print (str.title())
print (str.upper())
print (str.lower())
print (str.strip('Today is'))
str2='---Hello World***'
print (str2.lstrip('-'))
